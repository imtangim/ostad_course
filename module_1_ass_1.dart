void main() {
  const num a = 20;
  const num b = 11;

  print("a = ${a}, b =${b}");
  print("Addition: ${a} + ${b} = ${a + b}");
  print("Substraction: ${a} - ${b} = ${a - b}");
  print("Multiplication: ${a} * ${b} = ${a * b}");
  print("Division: ${a} / ${b} = ${a / b}");
  print("Modulus: ${a} % ${b} = ${a % b}");
}


/*
Output:- 
a = 20, b =11
Addition: 20 + 11 = 31
Substraction: 20 - 11 = 9
Multiplication: 20 * 11 = 220
Division: 20 / 11 = 1.8181818181818181
Modulus: 20 % 11 = 9
 */